package loopsconcept;

public class PrimeOrNot {

	public static void main(String[] args) {
		System.out.println("Check whether the number entered is prime or not.");
		int a= 567;
		if (a%2 !=0){
			System.out.println("The number entered " +a+ " is a prime.");
		}
		else{
			System.out.println("The number entered " +a+ " is not a prime");
		}
			
	}

}
